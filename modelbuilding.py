"""### Model Performance Comparison"""

from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
import matplotlib.pyplot as plt
import numpy as np

def evaluate_regression_model(y_true, y_pred, model_name):
    mae = mean_absolute_error(y_true, y_pred)
    mse = mean_squared_error(y_true, y_pred)
    rmse = np.sqrt(mse)
    r2 = r2_score(y_true, y_pred)

    print(f"--- {model_name} ---")
    print(f"MAE: {mae:.2f}")
    print(f"MSE: {mse:.2f}")
    print(f"RMSE: {rmse:.2f}")
    print(f"R-squared: {r2:.2f}")
    print("\n")

    plt.figure(figsize=(8, 6))
    plt.scatter(y_true, y_pred, alpha=0.3)
    plt.plot([y_true.min(), y_true.max()], [y_true.min(), y_true.max()], 'r--', lw=2)
    plt.xlabel("Actual Salary")
    plt.ylabel("Predicted Salary")
    plt.title(f"{model_name}: Actual vs. Predicted Salary")
    plt.grid(True)
    plt.show()


evaluate_regression_model(y_test, y_pred_linear_reg, "Linear Regression")
evaluate_regression_model(y_test, y_pred_svm, "Support Vector Machine (SVM)")
evaluate_regression_model(y_test, y_pred_knn, "K-Nearest Neighbors (KNN)")
evaluate_regression_model(y_test, y_pred_decision_tree, "Decision Tree")
evaluate_regression_model(y_test, y_pred_random_forest, "Random Forest")



